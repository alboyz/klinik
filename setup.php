 */

// Check if setup has already been run
if (file_exists('config/.setup_complete')) {
    die('Setup has already been completed. Delete config/.setup_complete to run setup again.');
}

$message = '';
$error = '';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $db_host = $_POST['db_host'] ?? 'localhost';
    $db_name = $_POST['db_name'] ?? 'pet_clinic';
    $db_user = $_POST['db_user'] ?? 'root';
    $db_pass = $_POST['db_pass'] ?? '';
    $admin_username = $_POST['admin_username'] ?? 'admin';
    $admin_password = $_POST['admin_password'] ?? '';
    $admin_email = $_POST['admin_email'] ?? 'admin@petclinic.com';
    $admin_name = $_POST['admin_name'] ?? 'System Administrator';
    $install_sample_data = isset($_POST['install_sample_data']);

    try {
        // Test database connection
        $pdo = new PDO("mysql:host=$db_host", $db_user, $db_pass);
        $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

        // Create database if it doesn't exist
        $pdo->exec("CREATE DATABASE IF NOT EXISTS `$db_name` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
        $pdo->exec("USE `$db_name`");

        // Read and execute schema
        $schema = file_get_contents('database/schema.sql');
        $pdo->exec($schema);

        // Create admin user
        $password_hash = password_hash($admin_password, PASSWORD_DEFAULT);
        $stmt = $pdo->prepare("INSERT INTO users (username, password_hash, full_name, email, role, status) VALUES (?, ?, ?, ?, 'admin', 'active')");
        $stmt->execute([$admin_username, $password_hash, $admin_name, $admin_email]);

        // Install sample data if requested
        if ($install_sample_data) {
            $sample_data = file_get_contents('database/sample_data.sql');
            // Remove the admin user insert from sample data to avoid conflict
            $sample_data = preg_replace("/INSERT INTO users.*?'admin'.*?;/s", "", $sample_data);
            $pdo->exec($sample_data);
        }

        // Create database configuration file
        $config_content = "<?php
/**
 * Database Configuration
 * Generated by setup script on " . date('Y-m-d H:i:s') . "
 */

define('DB_HOST', '$db_host');
define('DB_NAME', '$db_name');
define('DB_USER', '$db_user');
define('DB_PASS', '$db_pass');
define('DB_CHARSET', 'utf8mb4');

/**
 * Application Configuration
 */
define('APP_NAME', 'Ruang Fauna');
define('APP_VERSION', '1.0.0');
define('TIMEZONE', 'Asia/Jakarta');

/**
 * Security Configuration
 */
define('SESSION_LIFETIME', 3600); // 1 hour
define('PASSWORD_MIN_LENGTH', 6);

/**
 * File Upload Configuration
 */
define('UPLOAD_MAX_SIZE', 5 * 1024 * 1024); // 5MB
define('ALLOWED_EXTENSIONS', ['jpg', 'jpeg', 'png', 'gif', 'pdf', 'doc', 'docx']);

/**
 * Pagination Configuration
 */
define('RECORDS_PER_PAGE', 20);

/**
 * Currency Configuration
 */
define('CURRENCY_SYMBOL', 'Rp');
define('CURRENCY_CODE', 'idr');
?>";

        file_put_contents('config/database.php', $config_content);

        // Mark setup as complete
        file_put_contents('config/.setup_complete', date('Y-m-d H:i:s'));

        $message = "Setup completed successfully! You can now <a href='index.php'>login to the application</a> using your admin credentials.";
    } catch (PDOException $e) {
        $error = "Database error: " . $e->getMessage();
    } catch (Exception $e) {
        $error = "Setup error: " . $e->getMessage();
    }
}
